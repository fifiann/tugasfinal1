    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Nama.disable();
        int i = jTable1.getSelectedRow();
        
        if (i == -1) {
            return;
        } else {
            String nama = (String) model.getValueAt(i, 0);
            Nama.setText(nama);
            String film = (String) model.getValueAt(i, 1);
            jComboBox1.setSelectedItem(film);
            if (jTable1.getValueAt(i, 2).equals("Reguler")) {
                reguler.setSelected(true);
                atmos.setSelected(false);
                ultra_xd.setSelected(false);
                vip.setSelected(false);
            } else if (jTable1.getValueAt(i, 2).equals("Ultra XD Atmos")) {
                reguler.setSelected(false);
                atmos.setSelected(true);
                ultra_xd.setSelected(false);
                vip.setSelected(false);
            } else if (jTable1.getValueAt(i, 2).equals("Ultra XD")) {
                reguler.setSelected(false);
                atmos.setSelected(false);
                ultra_xd.setSelected(true);
                vip.setSelected(false);
            } else if (jTable1.getValueAt(i, 2).equals("VIP")) {
                reguler.setSelected(false);
                atmos.setSelected(false);
                ultra_xd.setSelected(false);
                vip.setSelected(true);
            }
            String jam = (String) model.getValueAt(i, 3);
            jamTayang.setSelectedItem(jam);
            String harga = (String) model.getValueAt(i, 4);
            hargaSatuan.setText(harga);
            String jml = (String) model.getValueAt(i, 5);
            JumlahBeli.setText(jml);
            String total = (String) model.getValueAt(i, 6);
            Total_beli.setText(total);
        }
    }//GEN-LAST:event_jTable1MouseClicked

