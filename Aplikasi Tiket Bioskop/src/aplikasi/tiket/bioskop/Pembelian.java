    public void LoadData() {
        model = new DefaultTableModel();
        jTable1.setModel(model);
        model.addColumn("NAMA");
        model.addColumn("FILM");
        model.addColumn("JENIS TIKET");
        model.addColumn("JAM TAYANG");
        model.addColumn("HARGA");
        model.addColumn("JUMLAH");
        model.addColumn("TOTAL");
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection c = (Connection) ClassDB.getKoneksi();
            Statement s = (Statement) c.createStatement();
            String sql = "SELECT * FROM pembelian";
            ResultSetImpl r = (ResultSetImpl) s.executeQuery(sql);
            
            while (r.next()) {                
                Object[] o = new Object[15];
                o[0] = r.getString("nama");
                o[1] = r.getString("film");
                o[2] = r.getString("jenis_tiket");
                o[3] = r.getString("jam_tayang");
                o[4] = r.getString("harga");
                o[5] = r.getString("jml");
                o[6] = r.getString("total");
                model.addRow(o);              
            }
            for (int i = 0; i < model.getColumnCount(); i++) {
                TableColumn column = jTable1.getColumnModel().getColumn(i);
                if (i == 0) column.setPreferredWidth(150);
                if (i == 1) column.setPreferredWidth(150);
                if (i == 2) column.setPreferredWidth(180);
                if (i == 3) column.setPreferredWidth(180);
                if (i == 4) column.setPreferredWidth(100);
                if (i == 5) column.setPreferredWidth(100);
                if (i == 6) column.setPreferredWidth(180);
                
            }
            r.close();
            s.close();
  
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
 
    }
